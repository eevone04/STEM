    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        int id = Integer.parseInt(Id.getText());
        String sql = "SELECT * FROM quiz_create WHERE id = ?";
        try {
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection connection = DriverManager.getConnection(
                "jdbc:mysql://localhost:3306/quiz",
                "root", "SQL123123");
            PreparedStatement pst = connection.prepareStatement(sql);
            pst.setInt(1, id);
            ResultSet rs = pst.executeQuery();
            DefaultTableModel model = (DefaultTableModel)QuizTable.getModel();
            model.setRowCount(0);
            boolean found = false;
            while (rs.next()) {
                if(rs.getString(6).equals(loggedInUsername)){
                found = true;
                model.addRow(new String[]{
                    rs.getString(1), rs.getString(2), rs.getString(3), rs.getString(4)
                });
                }
            }

            // If no records were found, show a message
            if (!found) {
                JOptionPane.showMessageDialog(this, "No record found for ID: " + id);
            }
        } catch(Exception ex){
            System.out.println("Error: "+ex.getMessage());
        }
    }//GEN-LAST:event_jButton1ActionPerformed

