    private void DeleteButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DeleteButtonActionPerformed
        // Get the ID from the text field
    int id = Integer.parseInt(Id.getText());
    
    // Prepare the SQL delete statement
    String sql = "DELETE FROM quiz_create WHERE id = ?";
    
    try {
        // Connect to the database
        Class.forName("com.mysql.cj.jdbc.Driver");
        Connection connection = DriverManager.getConnection(
            "jdbc:mysql://localhost:3306/quiz",
            "root", "SQL123123");
        
        // Create a prepared statement with the SQL delete statement
        PreparedStatement pst = connection.prepareStatement(sql);
        
        // Set the ID parameter for the prepared statement
        pst.setInt(1, id);
        
        // Execute the delete operation
        int rowsDeleted = pst.executeUpdate();
        
        // Check if any rows were deleted
        if (rowsDeleted > 0) {
            JOptionPane.showMessageDialog(this, "Quiz Deleted Successfully");
        } else {
            JOptionPane.showMessageDialog(this, "No record found for ID: " + id);
        }
        
        // Close the database connection
        connection.close();
    } catch (Exception ex) {
        System.out.println("Error: " + ex.getMessage());
    }
    }//GEN-LAST:event_DeleteButtonActionPerformed

