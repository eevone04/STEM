    public void search(){
        String searchText = QuizTitle.getText();
        if (!searchText.isEmpty()) {
        clear();
    }
        int id = Integer.parseInt(Id.getText());
        String sql = "SELECT * FROM quiz_create WHERE id = ?";
        try {
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection connection = DriverManager.getConnection(
                "jdbc:mysql://localhost:3306/quiz",
                "root", "SQL123123");
            PreparedStatement pst = connection.prepareStatement(sql);
            pst.setInt(1, id);
            ResultSet rs = pst.executeQuery();

            if (rs.next()) {
                    if(rs.getString(6).equals(loggedInUsername)){
                    QuizTitle.setText(rs.getString("Title"));
                    QuizDes.setText(rs.getString("Description"));
                    QuizTheme.setSelectedItem((String)rs.getString("Theme"));
                    QuizContent.setText(rs.getString("Content"));
                    }
                 else {
                    // If no record found
                    clear();
                    JOptionPane.showMessageDialog(this, "No record found for ID: " + id);
                } 
            }else {
                clear();
                JOptionPane.showMessageDialog(this, "No record found for ID: " + id);
            }

            connection.close();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
        }
    }
    public void clear(){
        QuizTitle.setText("");
        QuizDes.setText("");
        QuizTheme.setSelectedIndex(0);
        QuizContent.setText("");
    }
