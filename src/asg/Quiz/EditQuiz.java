    private void UpdateButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_UpdateButtonActionPerformed
        quizTheme = (String)QuizTheme.getSelectedItem();
        if(QuizTitle.getText().isEmpty()||QuizDes.getText().isEmpty() ||QuizContent.getText()==null){
            JOptionPane.showMessageDialog(this, "Please make sure title, description or content is not blank!");
        }
        else{
        try {
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection connection = DriverManager.getConnection(
                "jdbc:mysql://localhost:3306/quiz",
                "root", "SQL123123");
            System.out.println("Success");
            String sql = "UPDATE quiz_create SET Title=?, Description=?, Theme=?, Content=? WHERE id=?";
            PreparedStatement pstmt = connection.prepareStatement(sql);
            
            // Set values for the event attributes
            pstmt.setString(1, quizTitle);
            pstmt.setString(2, quizDes);
            pstmt.setString(3, quizTheme);
            pstmt.setString(4, quizContent);
            pstmt.setInt(5, Integer.parseInt(Id.getText()));
            
            // Execute the SQL statement
            int rowsInserted = pstmt.executeUpdate();
            if (rowsInserted > 0) {
                System.out.println("A new quiz was inserted successfully.");
            }
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(Asg.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(Asg.class.getName()).log(Level.SEVERE, null, ex);
        }
        JOptionPane.showMessageDialog(this, "Quiz Added");
        }
    }//GEN-LAST:event_UpdateButtonActionPerformed

